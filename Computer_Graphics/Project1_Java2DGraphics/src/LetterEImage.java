    public BufferedImage getImage(int[][] array) {
        BufferedImage image = new BufferedImage(XSIZE, YSIZE, BufferedImage.TYPE_INT_RGB);
        for (int x = 0; x < XSIZE; x++) {
            for (int y = 0; y < YSIZE; y++) {
                int pixelColor = array[x][y];
                if (pixelColor == 0) {
                    pixelColor = Color.WHITE.getRGB();
                } else {
                    pixelColor = Color.BLACK.getRGB();
                }
                image.setRGB(x, y, pixelColor);
            }
        }
        return image;
    }
